-- Процедура авторизации пользователя в БД
DELIMITER //
CREATE PROCEDURE F_UserAuth (vk BIGINT)
BEGIN
	DECLARE person INT;
	DECLARE max_id INT;
	SET person = (SELECT Person_ID FROM Person WHERE VK_ID = vk);
	IF person IS NULL THEN
		SET max_id = (SELECT MAX(Person_ID) + 1 FROM Person);
        IF max_id IS NULL THEN
			INSERT INTO Person (VK_ID, Person_ID) VALUES (vk, 1);
        ELSE
			INSERT INTO Person (VK_ID, Person_ID) VALUES (vk, max_id);
		END IF;
	END IF;
END; //
DELIMITER ;


-- Соединение функций do-result и update-answer в одну процедуру
DELIMITER //
CREATE PROCEDURE F_DoResultUpdateAnswers (vk_identity BIGINT, test_identity INT)
BEGIN
	-- бывший do-result
	INSERT INTO result(VK_ID, Test_ID, Factor, Value, Reply_Date)  
		SELECT nwt.VK_ID, nwt.Test_ID, nwt.Factor, nwt.Value, nwt.Reply_Date
		FROM
			(SELECT pa.VK_ID, q.Test_ID, pa.Result_ID, q.Category as Factor, SUM(a.Value) as Value, NOW() as Reply_Date 
			FROM person_answer as pa
				LEFT JOIN answer as a ON pa.answer_id = a.answer_id
				LEFT JOIN question as q ON a.question_id = q.question_id
					WHERE q.Test_ID = test_identity AND pa.VK_ID = vk_identity 
						AND pa.result_id = 0 AND q.A_Mode = 'single'
						GROUP BY q.Category) as nwt;
                        
	-- бывший update-person-answer (таблица person-asnwer)
    UPDATE person_answer as nt 
		SET nt.Result_ID = 1
			WHERE nt.Person_Answer_ID IN 
				(SELECT nw.Person_Answer_ID FROM 
				(SELECT pa.person_answer_id  
					FROM person_answer as pa   
						JOIN answer as a ON a.answer_id = pa.answer_id   
						JOIN question as q ON q.question_id = a.question_id    
							WHERE pa.vk_id = vk_identity AND q.test_id = test_identity
								AND pa.result_id = 0 AND q.A_Mode = 'single') as nw);
                                
    -- бывший update-person-answer (таблица person-multianswer)
    UPDATE Person_MultiAnswer SET Status = 1 
		WHERE Question_ID IN (SELECT NT.Question_ID FROM (SELECT PMA.Question_ID FROM Person_MultiAnswer AS PMA, Question AS Q 
			WHERE PMA.Question_ID =  Q.Question_ID AND Test_ID = test_identity AND Q.A_Mode = 'multiple'
			GROUP BY PMA.Question_ID) AS NT)
				AND VK_ID = vk_identity AND Status = 0;
END; //
DELIMITER ;



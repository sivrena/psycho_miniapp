USE heroku_...;

SET @@auto_increment_increment=10;

-- DEPLOY ALL TABLES

CREATE TABLE Person (
	VK_ID	    BIGINT PRIMARY KEY,
    Person_ID	INTEGER NOT NULL
);

CREATE TABLE Test (
	Test_ID	    INT PRIMARY KEY AUTO_INCREMENT,
    Name	    VARCHAR(200) NOT NULL,
    Instruction TEXT,
    CanRedo     INTEGER DEFAULT 0
);

CREATE TABLE Question (
	Question_ID	INT PRIMARY KEY AUTO_INCREMENT,
    Test_ID	    INTEGER,
    Description	VARCHAR(5000) NOT NULL,
    Category	VARCHAR(500) NOT NULL,
    Type        VARCHAR(50),
    A_Mode      VARCHAR(8),
    Photo       VARCHAR(1000),
    FOREIGN KEY (Test_ID)  REFERENCES Test (Test_ID)
);

CREATE TABLE Answer(
    Answer_ID	INT PRIMARY KEY AUTO_INCREMENT,
    Question_ID INTEGER,
    Description	VARCHAR(1000) NOT NULL,
    Value	    INTEGER NOT NULL,
    FOREIGN KEY (Question_ID) REFERENCES Question (Question_ID)
);

CREATE TABLE Person_Answer(
    Person_Answer_ID	INT PRIMARY KEY AUTO_INCREMENT,
    VK_ID	    BIGINT,
    Answer_ID	INTEGER,
    Reply_Date 	DATETIME,
    Result_ID   INT DEFAULT 0,
    FOREIGN KEY (VK_ID)  REFERENCES Person (VK_ID),
    FOREIGN KEY (Answer_ID)  REFERENCES Answer (Answer_ID)
);

CREATE TABLE Result(
    Result_ID	INT PRIMARY KEY AUTO_INCREMENT,
    VK_ID	    BIGINT,
    Test_ID	    INTEGER,
    Factor	    VARCHAR(1000),
    Value	    INTEGER NOT NULL,
    Reply_Date  DATETIME,
    FOREIGN KEY (VK_ID) REFERENCES Person (VK_ID),
    FOREIGN KEY (Test_ID)   REFERENCES Test (Test_ID)
);

CREATE TABLE Person_MultiAnswer(
    Person_MultiAnswer_ID	INT PRIMARY KEY AUTO_INCREMENT,
    VK_ID		BIGINT,
    Answer		VARCHAR(500),
    Reply_Date 	DATETIME,
    Status   	INT DEFAULT 0,
    Question_ID	INTEGER,
    FOREIGN KEY (VK_ID)	REFERENCES Person (VK_ID),
    FOREIGN KEY (Question_ID) REFERENCES Question (Question_ID)
);

